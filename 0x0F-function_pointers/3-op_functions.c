#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - returns sum
 * @a: first parameter
 * @b: second parameter
 *
 * Description: returns sum
 * Return: Always(0) Success
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference
 * @a: first parameter
 * @b: second parameter
 *
 * Description: returns difference
 * Return: Always(0) Success
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product
 * @a: first parameter
 * @b: second parameter
 *
 * Description: returns product
 * Return: Always(0) Success
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns quotient
 * @a: first parameter
 * @b: second parameter
 *
 * Description: returns quotient
 * Return: Always(0) Success
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder
 * @a: first parameter
 * @b: second parameter
 *
 * Description: returns remainder
 * Return: Always(0) Success
 */

int op_mod(int a, int b)
{
	return (a % b);
}
